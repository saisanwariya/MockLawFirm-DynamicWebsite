            <h3>Criminal</h3>
            <div class="red-bar"></div>
          <p>Pearson Spector Litt has one of the world’s elite investigations, government enforcement and white collar criminal defense practices.  Over 50 partners in a dozen offices worldwide are experts in the field and our firm has repeatedly been recognized as being among the very best by numerous legal publications, including Chambers USA (2017), Chambers UK (2017), Law360 (2016), The American Lawyer (2017), The National Law Journal (2017), The International Who’s Who of Business Lawyers (Law Business Research Ltd., 2017, 2016), Legal 500(2017, 2016, 2015,  2014, 2013, 2012, 2011), and Best Lawyers (Woodward White, inc., 2016, 2015, 2013).  Notably, our practice group was named “Transatlantic Investigations Team of the Year” by Legal Week and The American Lawyer at the 2017 Transatlantic Legal Awards,  “Most Impressive Investigations Practice of the Year” by the Global Investigations Review in 2015, and “White Collar Practice Group of the Year” by Law360 for both 2015 and 2016.</p>
            
            <p>Deep experience with government investigations.  In government-facing matters, experience as a prosecutor is important.  Over 25 of our partners have served as attorneys for the U.S. Department of Justice (the “DOJ”), United States Attorneys (for the chief prosecutor for the United States in each federal district), Assistant United States Attorneys, attorneys for the UK’s Serious Fraud Office (the “SFO”), or other high ranking government lawyers. Given this valuable experience, our partners understand the dynamics of a prosecutor’s charging decision.  As a result, we have considerable expertise in designing and implementing strategies to avert criminal charges. </p>
         
         <p>We focus on getting the best resolutions as quickly as possible.  Of course, the best resolution of a criminal matter is achieved when no charges are ever brought, whether because a successful internal investigation provides the basis for a negotiated resolution or because presenting a vigorous defense early shows the government authority the downside to pursuing prosecution.  We recognize that the mere filing of a criminal charge can ruin a client’s reputation and trigger catastrophic financial losses.  While Pearson Spector Litt has regularly represented clients in high profile matters covered on the front pages of The New York Times, The Wall Street Journal, Financial Times and other publications, our best results have often been obtained quietly, outside the public glare, by persuading government lawyers to refrain from filing charges.  To effectively conduct these negotiations, it is critical that the lawyer has earned the respect of the prosecutors, which requires mastery of the process.  Our partners’ extensive experience with the government gives us that expertise.</p>   
          <p>Our experience in corporate investigations, government enforcement and white collar defense is wide-ranging and diverse.  We have conducted sensitive internal investigations at the direction of corporate General Counsel, Audit Committees and Special Committees.  We have successfully represented both corporations and individuals in congressional investigations, grand jury investigations, at trial and on appeal.  We have conducted internal investigations regarding and defended clients against, allegations of health care, securities and government contract fraud, Foreign Corrupt Practices Act (“FCPA”) violations, Corruption, Money Laundering and Trade Sanctions, Economic Espionage and Trade Secrets Theft, Tax Evasion, Fraud, Asset Forfeiture, Employment and Immigration Issues, Environmental Violations, and violations of the Anti-Kickback Statute, the False Claims Act, and Criminal Antitrust statutes.  We have litigated against virtually every U.S. enforcement agency including the DOJ, the Securities and Exchange Commission (the “SEC”), the U.S. Department of the Treasury and its Office of Foreign Assets Control (“OFAC”), the Federal Trade Commission (the “FTC”), as well as Attorneys General and District Attorneys in numerous states.  Likewise, we frequently advise clients in connection with international extradition matters, and in proceedings before the International Criminal Police Organization (“INTERPOL”) and the Inter-American Commission on Human Rights (“ICHR”).</p>
          
          <p>We have an international practice with partners located in 19 global offices, and have represented clients around the world, including in the United States, and throughout the world, including Latin America, Asia, Australia, Europe, the Middle East and Russia. Much of our work has been done on behalf of foreign companies and individuals whose activities have drawn the attention of U.S. regulators and prosecutors.  Our global resources, coupled with our unique cross-border experience, allow us to effectively and efficiently navigate related criminal investigations in multiple jurisdictions.</p>
            
